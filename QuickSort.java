    public static void qsortH(int[] arr, int l, int r){
	if (l >= r)
	    return;
	int s = partition(arr, l, r, l);
	if(l < s - 1)
	    qsortH(arr, l, s-1);
	if(s < r)
	    qsortH(arr, s + 1, r);
    }

    
